#include "ft_printf.h"

char	*ft_c(va_list ap)
{	
	char	*buff;

	buff = malloc(2);
	if (!buff)
		return (0);
	*buff = va_arg(ap, int);
	*(buff + 1) = '\0';
	return (buff);
}
